package com.ktds.artist.vo;

public class ArtistVO {
	private int artistId;
	private String artistName;
	private String nationality;
	private String activityType;
	private String activityGeneration;
	private String activityGenre;
	private String debutSong;
	private String artistBirthday;
	private int magazineId;
	private String id;
	private String artistImage;
	
	public int getArtistId() {
		return artistId;
	}
	
	public void setArtistId(int artistId) {
		this.artistId = artistId;
	}
	
	public String getArtistName() {
		return artistName;
	}
	
	public void setArtistName(String artistName) {
		this.artistName = artistName;
	}
	
	public String getNationality() {
		return nationality;
	}
	
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	
	public String getActivityType() {
		return activityType;
	}
	
	public void setActivityType(String activityType) {
		this.activityType = activityType;
	}
	
	public String getActivityGeneration() {
		return activityGeneration;
	}
	
	public void setActivityGeneration(String activityGeneration) {
		this.activityGeneration = activityGeneration;
	}
	
	public String getActivityGenre() {
		return activityGenre;
	}
	
	public void setActivityGenre(String activityGenre) {
		this.activityGenre = activityGenre;
	}
	
	public String getDebutSong() {
		return debutSong;
	}
	
	public void setDebutSong(String debutSong) {
		this.debutSong = debutSong;
	}
	
	public String getArtistBirthday() {
		return artistBirthday;
	}
	
	public void setArtistBirthday(String artistBirthday) {
		this.artistBirthday = artistBirthday;
	}
	
	public int getMagazineId() {
		return magazineId;
	}
	
	public void setMagazineId(int magazineId) {
		this.magazineId = magazineId;
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getArtistImage() {
		return artistImage;
	}

	public void setArtistImage(String artistImage) {
		this.artistImage = artistImage;
	}

}

